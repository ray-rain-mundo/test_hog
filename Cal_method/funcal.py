class Calculator:

    def add(self, a, b):
        self.result_add = a + b
        return self.result_add

    def div(self, a, b):
        self.result_div = a / b
        return self.result_div

    def sub(self,a,b):
        self.result_sub = a - b
        return self.result_sub

    def mul(self,a,b):
        self.result_mul = a * b
        return  self.result_mul


